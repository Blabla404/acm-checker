</ul>

<h1>Ressources pour le site Uva</h1>
<ul>
  <li><a href="http://uhunt.felix-halim.net/">uHunt</a>: Des outils pour le site Uva, en particulier, le suivi en temps réel de vos soumissions avec le lien direct du problème sur le forum. Il y a également une catégorisation d'une bonne partie des problèmes, utile pour s'entraîner sur un thème donné.</li>
  <li><a href="http://uvatoolkit.com/">Uvatoolkit</a>: Permet de calculer la sortie attendue d'un problème pour une entrée que vous devez lui fournir. </li>
  <li><a href="http://online-judge.uva.es/board/">Le forum Uva</a>: Généralement les "pièges" de chaque problème y sont discutés. Une bonne manière de trouver les cas critiques en cas de WRONG ANSWER.</li>
</ul>

